
public class Static_Var_Initialization {

	
	static 	int a;
	        int b=10;
	
	static  float c;
			float d=20f;
	
	static  char e;
			char f='a';
    
	static  double g;
			double h=30d;
	
	static  short i;
			short j=40;
	
	static  byte k;
			byte l=50;

	static  long m;
			long n=60l;
	
	static boolean o;
		   boolean p=true;
		   
	
	public static void main(String arg[])
	{
		
		Static_Var_Initialization z=new Static_Var_Initialization();
		
		
		System.out.println("a"+a);
		System.out.println("b"+z.b);
		System.out.println("c"+c);
		System.out.println("d"+z.d);
		System.out.println("e"+e);
		System.out.println("f"+z.f);
		System.out.println("g"+g);
		System.out.println("h"+z.h);
		System.out.println("i"+i);
		System.out.println ("j"+z.j);
		System.out.println("k"+k);
		System.out.println("l"+z.l);
		System.out.println("m"+m);
		System.out.println("n"+z.n);
		System.out.println("o"+o);
		System.out.println("p"+z.p);
		
		
		
	}
	
	
}
