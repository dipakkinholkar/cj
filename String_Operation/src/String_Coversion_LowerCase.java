
public class String_Coversion_LowerCase {

	public static void main(String[] args) {
		
		// for UpperCase to LowerCase Conversion.
		String string="KADAM DIPAK MAROTI";
		string=string.toLowerCase();
		System.out.println("U to L :"+string);
		
		// for LowerCase to UpperCase Conversion.
		
		string=string.toUpperCase();
		System.out.println("L to U :"+string);
		
		

	}

}
