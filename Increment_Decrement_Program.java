


public class Increment_Decrement_Program {
    
    public static void main(String[] args)
    {
     int a=1;
     
     // Post_Increment of a variable.---------- 
     
     System.out.println("a="+a);
     a++;
     System.out.println("a++:"+a);
     a--;
     System.out.println("a--:"+a);
     
    // Pre_Increment of a variable.-------------
     System.out.println("a:"+a);
     ++a;
     System.out.println("++a:"+a);
     --a;
     System.out.println("--a:"+a);
     //----------------------------------------
     int b=10;
     int c;
     //-----------post_increment-----------------------------
     System.out.println("");
     c=b++;
     System.out.println("c=b++:"+b);
     //-----------pre_increment-----------------------------
     System.out.println("");
     c=++b;
     System.out.println("c=++b:"+b);
     
     
     
        
        
    
    }
    
}
